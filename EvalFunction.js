     getPieceValue = function (piece, x, y) {
        if (piece.type  === null) {
            return 0;
        }
        var getAbsoluteValue = function (piece, isWhite, x, y) {
            if (piece.type  === 'p') {
                return 10 + (isWhite ? pawnEvalWhite[y][x] : pawnEvalBlack[y][x]);
            } else if (piece.type  === 'r') {
                return 50 + (isWhite ? rookEvalWhite[y][x] : rookEvalBlack[y][x]);
            } else if (piece.type  === 'n') {
                return 30 + knightEval[y][x];
            } else if (piece.type === 'b') {
                return 30 + (isWhite ? bishopEvalWhite[y][x] : bishopEvalBlack[y][x]);
            } else if (piece.type  === 'q') {
                return 90 + evalQueen[y][x];
            } else if (piece.type  === 'k') {
                return 900 + (isWhite ? kingEvalWhite[y][x] : kingEvalBlack[y][x]);
            }
            console.log("UKNOWN PIECE");
            throw "Unknown piece: " + piece.type;
        };

        var absoluteValue = getAbsoluteValue(piece, piece.color === 'w', x, y);
        return piece.color === 'w' ? absoluteValue : -absoluteValue;
    };

     getBoardEval = function (pieces) {
        for (let i = 0; i < pieces.length; i++) {
            const xy = pieces[i].position.split('');
            const x = xy[0].charCodeAt(0) - 97;
            const y = Math.abs(xy[1] - 8)
            pieces[i].value = getPieceValue(pieces[i],  x, y)
        }
         const boardEval = pieces.reduce((a, b) => a + b.value,0); 
          return boardEval   
    }
